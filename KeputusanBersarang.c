#include <stdio.h>

int main() {
    int nilai;

    printf("Masukkan nilai: ");
    scanf("%d", &nilai);

    if (nilai >= 101) {
        printf ("peringkat: TIDAK VALID (NGECHEAT IKI)\n");
    } else if (nilai >= 85) {
        printf ("peringkat: A\n");
    } else if (nilai >= 70) {
        printf("Peringkat: B\n");
    } else if (nilai >= 55) {
        printf("Peringkat: C\n");
    } else if (nilai >= 40) {
        printf("Peringkat: D\n");
    } else {
        printf("Peringkat: E\n");
    }

    return 0;
}
